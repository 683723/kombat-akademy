<h2>Fatal Blow</h2>

<div>
	<?php include_file('video.php', array(null, 'Performing a Fatal Blow (<span class="inputs btn-inputs-xs">SS+EX</span>) to deal massive damage.', 'https://www.youtube.com/watch?v=nBA7ZnxxHi8', 'right', true)) ?>

	<p><b>Fatal Blow</b> is a character's most powerful attack. In order to use a Fatal Blow, a player must be at 30% health or less and must press <span class="inputs">SS+EX</span>. An additional requirement is that the player's Kameo Fighter must not already be on the screen.</p>

	<p>Once used, a short cinematic sequence will play before the character performs their attack. If the Fatal Blow successfully connects, it'll deal substantial damage and cannot be combo broken, making it an amazing comeback move and round finisher. After the Fatal Blow hits, it cannot be used again for the remainder of the match.</p>

	<p>Fatal Blows gain a short amount of armor upon use, allowing the character to bypass enemy attacks. In some situations though, a well-timed attack will interrupt the Fatal Blow before the armor has time to take effect or after the armor has worn off. They are also very reactable because of the cinematic that plays upon use, so it's easy for opponents to quickly transition into blocking once they see the Fatal Blow. If blocked, Fatal Blows are highly punishable so it can be quite risky to throw them out. It's also worth noting that Fatal Blows cannot be used again for 10 seconds if they're blocked or if they miss.</p>
</div>