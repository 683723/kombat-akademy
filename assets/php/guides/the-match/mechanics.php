<h1>Mechanics</h1>

<h2>Blocking</h2>

<div>
	<p>Blocking is the act of defending against an opponent's attack, reducing the amount of damage taken. This is done by simply pressing the Block button. <b>Blocking is extremely important as it will prevent the opponent from landing hits, performing combos and dealing any serious damage.</b> Many moves if blocked will also leave the opponent at a disadvantage, allowing you to freely attack afterwards.</p>

	<p>Almost all attacks can be blocked depending on their <b>Block Type</b>:</p>

	<table style="width: 95%;">
		<tr>
			<th>Block Type</th>
			<th>Description</th>
		</tr>
		<tr>
			<td><b>High</b></td>
			<td>High attacks target the upper area of the opponent. These are easily blocked and can also be avoided by ducking without the need to block.</td>
		</tr>
		<tr>
			<td><b>Mid</b></td>
			<td>Mid attacks strike the middle area of the opponent and are the most common type of attacks. Like Highs, these are also blocked with ease, but cannot be ducked under.</td>
		</tr>
		<tr>
			<td><b>Low</b></td>
			<td>Low attacks target the lower part of the opponent. These must be blocked low, which is done by pressing the Block and Down buttons.</td>
		</tr>
		<tr>
			<td><b>Overhead</b></td>
			<td>Overhead attacks hit opponents who are blocking low. Unlike Low attacks, Overhead attacks must be blocked high.</td>
		</tr>
		<tr>
			<td><b>Throw</b></td>
			<td>Throws cannot be blocked but can be countered by performing a Throw Escape in retaliation to the throw. Throws can also be avoided by ducking underneath them.</td>
		</tr>
		<tr>
			<td><b>Unblockable</b></td>
			<td>Unblockable attacks cannot be blocked. Unblockable attacks are somewhat rare and are usually slow enough that they can be avoided on reaction.</td>
		</tr>
	</table>

