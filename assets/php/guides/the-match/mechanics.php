<h2>Breaker</h2>

<div>
	<?php include_file('image.php', array(null, 'Performing a Breaker (<span class="inputs btn-inputs-xs">F+EX</span>) to escape a combo.', '/images/guides/breaker.jpg', 'left', true)) ?>

	<p>Characters can escape from a combo by using a <b>Breaker</b>. This is done by pressing <span class="inputs">F+EX</span> while in a combo. Breakers cost 3 bars of Super Meter so they are quite expensive, but can save you from taking heavy damage. In addition, the player's Kameo Fighter must not already be on the screen before use. In general, <b>you should break as soon as possible to minimize the amount of damage taken</b>.</p>

	<p>Although Breakers can be valuable, there are times where it can be more beneficial to not break and instead save Super Meter for other purposes. Some players prefer not to break due to how resource-heavy Breakers are and would rather spend meter on enhancing Special Moves instead.</p>
</div>

