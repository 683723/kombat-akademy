<div>
	<?php include_file('video.php', array(null, 'Using an Enhanced Uppercut to follow up with a combo.', 'https://www.youtube.com/watch?v=3-5PKt3S3ps', 'left', true)) ?>

	<p>After landing a successful uppercut (<span class="inputs">D+2</span>), it can be enhanced by quickly jumping and pressing the <span class="inputs">EX</span> button. This will allow you to combo off of the uppercut for higher damaging punishes at the cost of 2 bars of Super Meter. The enhanced uppercut grants 4 hits of armor and disables the opponent's ability to use a Breaker or Kameo until the end of the combo. It can be a great way to end the round while preventing the opponent from escaping the combo.</p>

	<div class="clearfix"></div>
</div>

<h2>Armor</h2>

<div>
	<?php include_file('video.php', array(null, 'Using Sindel\'s Enhanced Karthweel (<span class="inputs btn-inputs-xs">DF4+EX</span>) to armor through Reptile\'s Slip-N-Died (<span class="inputs btn-inputs-xs">B+3,1,3</span>).', 'https://www.youtube.com/watch?v=W0Q0PIS2JP8', 'left', true)) ?>

	<p>Certain moves have a property called <b>Armor</b>. Moves with armor cannot be interrupted, making them powerful attacks to use while on the defensive. Typically, they are best used when at a disadvantage and the opponent is most likely to go on the offensive. An example of an armor move is <a href="/characters/sindel/">Sindel's</a> Enhanced Kartwheel (<span class="inputs">DF4+EX</span>).</p>

	<p>Armor moves usually cost 1 bar of Super Meter, and most are punishable if blocked. Since most armor moves can absorb only 1 hit, it's possible to <b>Armor Break</b> them by using a fast multi-hitting attack consisting of 2 or more hits. The only exceptions are Fatal Blows and attacks such as <a href="/characters/havik/">Havik's</a> Helping Hand (<span class="inputs">DB1</span>) which require additional hits in order to be broken.</p>
</div>

