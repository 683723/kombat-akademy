	<p>In general, the moves to watch out for most are Low and Overhead attacks as defending against one will leave you vulnerable to the other. Despite this, there are quite a few moves that are slow enough that they can be blocked on reaction. For instance, <a href="/characters/rain/">Rain's</a> H2Blow (<span class="inputs">F+3</span>) is a very slow Overhead attack and can be easily reacted to and blocked. While on the defensive, you'll want to stay alert and look for these kind of moves then block appropriately.</p>

	<p>Understanding the opponent's moveset and knowing how to block their attacks will prove to be invaluable when defending. Having a solid defense can make it extremely difficult for the opponent to open you up. At the same time, blocking too much will give them more opportunities to land a hit so it's important to maintain a strong balance between attacking and defending.</p>
</div>

<h2>Flawless Block</h2>

<div>
	<?php include_file('video.php', array(null, 'Flawless Blocking General Shao\'s Make Way (<span class="inputs btn-inputs-xs">B+2</span>) prevents taking block damage, stops the opponent from building Super Meter, and leaves him at -7 instead of +3.', 'https://www.youtube.com/watch?v=_ZrSExBpqfc', 'left', true)) ?>

	<p><b>Flawless Block</b> is a mechanic that occurs when a move is blocked at the moment of impact. When this happens, <b>zero damage is taken and the attacker does not build Super Meter</b>. Some moves when Flawless Blocked will also leave the attacker at a greater disadvantage than if the move were to be blocked normally. Flawless Blocking can be helpful in situations where you want to avoid taking block damage, such as when you are low on health. The downside to Flawless Blocking is that it can be rather difficult to perform consistently, and if mistimed, it'll result in being hit.</p>

	<div class="clearfix"></div>

	<div class="note">There is a 3 frame window to Flawless Block a move.</div>
</div>

<h2>Up Block</h2>

<div>
	<?php include_file('video.php', array(null, 'Using Up Block (<span class="inputs btn-inputs-xs">U+EX</span>) to punish a jump attack.', 'https://www.youtube.com/watch?v=lbDLXrOLznQ', 'left', true)) ?>

	<p><b>Up Block</b> is a method of blocking used to defend against Overhead attacks. This is done by pressing the Up button while blocking. When a move is Up Blocked, it'll leave the attacker at a greater disadvantage and in most cases fully punishable. The most common attacks to use Up Block against are jump attacks. Due to the fact that they hit as an Overhead on the way down, <b>the easiest and most consistent way to counter jump attacks is to use Up Block</b>.</p>

	<div class="clearfix"></div>

	<p>While Up Block acts as a great anti-air, it also comes with a few drawbacks. When performing an Up Block, you'll be unable to move for a short period of time. In addition, you'll be vulnerable to grabs and Low attacks while Up Blocking. If the enemy scouts an Up Block, they can opt to not perform their Overhead attack and instead go for a grab or Low attack to punish the Up Block.</p>
</div>

<h2>Throw</h2>

<div>
	<?php include_file('video.php', array(null, 'Throwing the opponent with an Away Throw (<span class="inputs btn-inputs-xs">THROW</span>) and Toward Throw (<span class="inputs btn-inputs-xs">F+THROW</span>).', 'https://www.youtube.com/watch?v=ieX30W-UeJQ', 'right', true)) ?>

	<p>A throw is a type of attack where the offender grabs the opponent to inflict damage while knocking them onto the ground. In order to perform a throw, the <span class="inputs">THROW</span> button must be pressed while the opponent is in close proximity. There are 2 types of throws: Toward Throw and Away Throw. By default, an Away Throw is performed which will switch positions with the opponent and send them in the opposite direction. To perform a Toward Throw, press <span class="inputs">F+THROW</span> which will throw the opponent forward.</p>

	<div class="clearfix"></div>

	<div class="tip">Throws are very useful for screen positioning. If the opponent is near the corner, use a Toward Throw to keep them on the same side. If you are near the corner, use an Away Throw to switch positions with the opponent and place them into the corner instead.</div>

	<p>Unlike ordinary attacks, <b>throws will connect regardless of whether the opponent is blocking or not, making them great for opening opponents up who tend to rely on blocking</b>. Although throws deal relatively low damage on their own, the damage from landing multiple grabs throughout the match adds up, especially if the opponent is unable to counteract them.</p>

	<div class="pros-cons">
		<div>
			<div>PROS</div>
			<ul>
				<li>Throws cannot be blocked and provide an easy way to deal damage against opponents who rely on blocking.</li>
				<li>Away Throws will switch positions with the opponent, allowing you to escape the corner.</li>
				<li>Some throws can be followed up with a combo if an Ambush Kameo is used.</li>
			</ul>
		</div>
		<div>
			<div>CONS</div>
			<ul>
				<li>Throws are highs, which means if the enemy ducks then the throw will miss, leaving the user punishable.</li>
				<li>They won't work if the opponent is airborne, so opponents who frequently jump will completely avoid being grabbed.</li>
				<li>If the opponent reacts quick enough, they can stop the throw with a Throw Escape.</li>
				<li>Throws are highly risky while providing little reward unless an Ambush Kameo is used.</li>
			</ul>
		</div>
	</div>

	<p>Throws, while being an easy way to net damage, should be used with extreme caution. Against opponents who know how to deal with them, attempting to go for a grab can be a huge risk.</p>

	<div class="note">A Toward Throw requires the player's Kameo Fighter to be available in order to be used. If the Kameo Fighter is still on the screen and a Toward Throw is used, the character will perform a shove instead, yielding minimal damage. In such situations, use an Away Throw instead.</div>
</div>

<h2>Throw Escape</h2>

<div>
	<?php include_file('video.php', array(null, 'Breaking a throw by using a Throw Escape on reaction to the enemy\'s throw.', 'https://www.youtube.com/watch?v=_dD0bv6Q7zA', 'left', true)) ?>

	<p>It is possible to break a throw by using a <b>Throw Escape</b>. This is done by quickly reacting to the throw animation then pressing a specific button depending on the kind of throw used. To counter an Away Throw, press the <span class="inputs">1</span> or <span class="inputs">3</span> button. Conversely, a Toward Throw can be prevented by pressing the <span class="inputs">2</span> or <span class="inputs">4</span> button.</p>

	<p>Performing a Throw Escape is ultimately a guess as to which type of throw the opponent will use. However, paying close attention to the screen position of both characters and the opponent's tendencies will allow you to predict which direction they'll likely throw in and react accordingly.</p>
</div>

