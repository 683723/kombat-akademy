<h1>The Match</h1>

<img alt="Fight Screen" src="/images/guides/fight-screen.jpg" />

<h2>Rounds</h2>

<div>
	<p>In <em>Mortal Kombat 1</em>, matches consist of 2 out of 3 rounds. This means that the opponent must be defeated twice in order to win the match. A match starts off with both characters in the center of the stage. Once the announcer says, <em>"Fight!"</em> both players are free to move. Unlike other fighting games, characters do not reset their positions to the center of the stage at the start of each round. For this reason, it's wise to maintain screen position before transitioning into the next round.</p>
</div>

<h2>Health</h2>

<div>
	<img alt="Fight Screen" src="/images/guides/healthbar.jpg" />

	<p>Each player has a health bar, where the amount of health depends on the character and Kameo chosen. The goal is to reduce the opponent's health to zero by hitting them with a combination of attacks. Most characters have a default value of 700 health points, while most Kameos have a default value of 300 health points. The total amount of health a player has is the combined health values of both the character and Kameo.</p>
</div>

<h2>Timer</h2>

<div>
	<img alt="Fight Screen" src="/images/guides/timer.jpg" />

	<p>The timer is located in the top-center of the screen. Each round lasts for 90 seconds. If neither player has depleted their opponent's health before the timer runs out, the player with more health is declared the winner of that round. In the event that both players have equal health when the timer runs out, the round ends in a draw. It's important to keep track of time throughout the match and aim to defeat the opponent or have greater health before the timer runs out.</p>
</div>

<h2>Super Meter</h2>

<div>
	<img alt="Fight Screen" src="/images/guides/super-meter.jpg" />

	<p>Near the bottom of the screen lies the <strong>Super Meter</strong>. This is mainly used for performing enhanced versions of attacks and utilizing mechanics such as Breaker. Super Meter is divided into 3 bars, with most enhanced attacks using up 1 bar and Breaker using all 3 bars. Super Meter is built by using Special Moves or when an attack is blocked by the opponent. Having good <strong>meter management</strong> is crucial, as it significantly expands your options against the opponent. It's advised to almost always have at least 1 bar of meter, saving meter and using it when the time is right.</p>
</div>

<h2>Kameo Meter</h2>

<div>
	<img alt="Fight Screen" src="/images/guides/kameo-meter.jpg" />

	<p>Underneath the health bar is the <strong>Kameo Meter</strong>. As the name suggests, this is used for Kameo attacks. Most Kameo attacks use up half of the Kameo Meter, while others use up the entire meter. Unlike Super Meter, Kameo Meter automatically recharges over time. The speed at which the Kameo Meter recharges varies between Kameo attacks. Using a combination of both Super Meter and Kameo Meter is highly recommended as it maximizes the strength of your available resources.</p>
</div>